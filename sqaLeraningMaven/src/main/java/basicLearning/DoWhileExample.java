package basicLearning;

public class DoWhileExample {

	public static void main(String[] args) {
		//this is while loop
		int j = 1;
		while(j < 1){
			System.out.println("value of j is : "+j);
			j++;
		}
		
		//This is do while
		int i = 1;
		do{
			System.out.println("value of i is : "+i);
			i++;
		}while(i < 1);		
	}
}
